package lt.ktu.dstrukov.scheduler.model;


public class MinMaxRequirement {

	private int min,max;
	
	public int getMin() {
		return min;
	}


	public int getMax() {
		return max;
	}



	
	
	public MinMaxRequirement(int minimum, int maximum) {
		min=minimum;
		max = maximum;
	}
	
	
	
	
	
}
